// Lookup: trouver un lieu par nom (recherche floue)
window.findCameroonLocation = function(query) {
  if (!query || query.trim().length < 2) return null;
  const q = query.trim().toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  // Exact match first
  let found = window.CAMEROON_LOCATIONS.find(l =>
    l.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '') === q
  );
  // Partial match
  if (!found) {
    found = window.CAMEROON_LOCATIONS.find(l =>
      l.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').includes(q) ||
      q.includes(l.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, ''))
    );
  }
  return found || null;
};

// Trouver les N points les plus proches d'une position GPS dans les données électorales
window.findNearestPoints = function(lat, lng, data, n = 10) {
  const all = [...data.elecam, ...data.bv].map(p => ({
    ...p,
    _dist: window.distanceKm(lat, lng, p.lat, p.lng)
  }));
  return all.sort((a, b) => a._dist - b._dist).slice(0, n);
};
